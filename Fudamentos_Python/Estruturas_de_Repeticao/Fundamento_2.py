# Estrutura de Repetição utilizando o FOR

lista = [1, 2, 3, 4, 5]

# percorrendo a lista e imprimindo os valores
print("Percorrendo a lista com FOR:\n")
for item in lista:
    print(item)

print("=" * 100)

# Estrutura de Repetição utilizando o WHILE

print("\nPercorrendo a lista com WHILE:\n")

contador = 0

while contador < 10:
    print(f'valor do contador é {contador}')
    contador += 1